package JavaClass12HW;

public class Methodlang {

       void lang(String name) {
		if(name.equalsIgnoreCase("Spain")) {
			System.out.println("Hola");
		}else if(name.equalsIgnoreCase("Germany")) {
			System.out.println("Hallo");
		}else if(name.equalsIgnoreCase("USA")) {
			System.out.println("Hello");
		}else if(name.equalsIgnoreCase("Afghanistan")) {
			System.out.println("Salam");
		}else if(name.equalsIgnoreCase("France")) {
			System.out.println("Bonjour");
		}else {
			System.out.println("Invalid Name");
		}
	}
}
